let season = "May";

switch (season) {
  case ("september", "October", "November"):
    season = "Autumn";
    break;
  case ("December", "January", "February"):
    season = "Winter";
    break;
  case ("March", "April", "May"):
    season = "Spring";
    break;
  case ("June", "JJuly", "August"):
    season = "Summer";
    break;
}

console.log(season);
